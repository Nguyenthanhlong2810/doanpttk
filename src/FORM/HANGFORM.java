    private void showNcc(){
        List<NhaCC> nccList=NhaCCModify.findAll();
        tableModel.setRowCount(0);
        nccList.forEach(ncc -> {
            tableModel.addRow(new Object[] {ncc.getMaNCC(),
                ncc.getTenNCC(),ncc.getSdt(),ncc.getDiaChi()     
            });
           
        });
    }
    private void showSelectedRow(){
        List<NhaCC> nccList=NhaCCModify.findAll();
        selectedIndex=tblNcc.getSelectedRow();
       
        if(selectedIndex>=0){
            NhaCC ncc=nccList.get(selectedIndex);
            txtMancc.setText(ncc.getMaNCC());
            txtTenncc.setText(ncc.getTenNCC());
            txtDiachi.setText(ncc.getDiaChi());
            txtSdt.setText(ncc.getSdt());
        }
    }
    private void showInfo(){
       List<NhaCC> nccList=NhaCCModify.findAll();
    
       try{
           NhaCC ncc=nccList.get(nccList.size()-1);
            txtMancc.setText(ncc.getMaNCC());
            txtTenncc.setText(ncc.getTenNCC());
            txtDiachi.setText(ncc.getDiaChi());
            txtSdt.setText(ncc.getSdt());
           
       }catch(Exception ex){
           txtMancc.setText("");
            txtTenncc.setText("");
            txtDiachi.setText("");
            txtSdt.setText("");
       }
        
    }
    

