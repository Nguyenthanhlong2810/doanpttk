    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String maNcc=txtMancc.getText();
        String tenNcc=txtTenncc.getText();
        String diaChi=txtDiachi.getText();
        String sdt=txtSdt.getText();
        boolean isOK=true;
        try{
            Double.parseDouble(sdt);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, "SỐ ĐIỆN THOẠI KHÔNG ĐƯỢC CHỨA KÍ TỰ HOẶC ĐỂ TRỐNG !");
            isOK=false;
        }
        if(NhaCCModify.compareByID(maNcc)){
            JOptionPane.showMessageDialog(rootPane, "MÃ NHÀ CUNG CẤP ĐÃ TỒN TẠI!");
            isOK=false;
            
        }
        if(isOK){
        NhaCC ncc=new NhaCC(maNcc,tenNcc,sdt,diaChi);
        
        NhaCCModify.insert(ncc);
        JOptionPane.showMessageDialog(rootPane, "LƯU THÀNH CÔNG!");
        }
        showNcc();
    }//GEN-LAST:event_btnSaveActionPerformed

