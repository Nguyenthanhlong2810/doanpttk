    public void selectedCTHD(String masp,int soluong){
        boolean isExisted=false;
        int vitritrung=-1;
        for(int i=0;i<cthdList.size();i++){
            if(cthdList.get(i).getMaSP().compareTo(masp)==0){
                isExisted=true;
                vitritrung=i;
            }
        }
        if(isExisted==false){
            TaoCode code=new TaoCode();
            String maCTHD=txtMahd.getText()+"CT"+code.createIdCTHD(cthdList);
            SanPham sp=SanPhamModify.findByID(masp);
            int SL=soluong;
            int thanhtien=SL*sp.getGiaXuat();
            ChiTietHoaDon cthd=new ChiTietHoaDon(maCTHD,txtMahd.getText(),sp.getMaSP(),SL,thanhtien);
            cthdList.add(cthd);
        }else{
            SanPham sp=SanPhamModify.findByID(masp);
            int soluongcu=cthdList.get(vitritrung).getSoLuong();
            int soluongmoi=soluongcu+soluong;
            if(soluongmoi>sp.getSoLuong()){
                JOptionPane.showMessageDialog(rootPane,"KHÔNG ĐỦ SỐ LƯỢNG SẢN PHẨM");
            }else{
            cthdList.get(vitritrung).setSoLuong(soluongmoi);
            cthdList.get(vitritrung).setThanhTien(soluongmoi*sp.getGiaXuat());

            }
        }
    }
