    private void btnThanhtoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhtoanActionPerformed
        // TODO add your handling code here:
        if(txtTonghoadon.getText().length()>0){
        boolean isOK=false;
        int tonghd=Integer.parseInt(txtTonghoadon.getText());
        HoaDon hd=new HoaDon(txtMahd.getText(),maKH,ngayBan,tonghd,ghiChu);
        HoaDonModify.insert(hd);

        for(int i=0;i<cthdList.size();i++){
            ChiTietHoaDonModify.insert(cthdList.get(i));
            }
        if(checkDiemtichluy.isSelected()){
            KhachHang kh=KhachHangModify.findByID(maKH);
            KhachHang kh_update=new KhachHang(kh.getMaKH(),kh.getTenKH(),kh.getDiaChi(),
            kh.getSdt(),0);
            KhachHangModify.update(kh_update);
        }
        //update so luong san pham:
        
        for(int i=0;i<cthdList.size();i++){
               SanPham sp=SanPhamModify.findByID(cthdList.get(i).getMaSP());
               int nsoLuong=sp.getSoLuong()-cthdList.get(i).getSoLuong();
               SanPham nsp=new SanPham(sp.getMaSP(),sp.getTenSP(),
                       sp.getGiaNhap(),sp.getGiaXuat(),nsoLuong,sp.getDonVi(),
                       sp.getMaLoaiSP(),sp.getMaNCC());
               SanPhamModify.update(nsp);

                 
        }
        //update diem dich luy sau khi thanh toan:
            KhachHang kh=KhachHangModify.findByID(maKH);
            int ndiemtichluy=kh.getDiemTichLuy()+(tonghd/100);
 
            KhachHang kh_update=new KhachHang(kh.getMaKH(),kh.getTenKH(),kh.getDiaChi(),
            kh.getSdt(),ndiemtichluy);
            KhachHangModify.update(kh_update);
        JOptionPane.showMessageDialog(rootPane, "LƯU THÀNH CÔNG !");
        this.dispose();
        new PHIEUHOADONFORM(txtMahd.getText(),txtChucvu.getText(),txtTongcong.getText(),
            txtMakh.getText(),txtNgayban.getText(),giamGia,txtTonghoadon.getText(),cthdList).setVisible(true);
        }else{
            JOptionPane.showMessageDialog(rootPane, "BẠN CHƯA ẤN THANH TOÁN!");
        }
        

    }//GEN-LAST:event_btnThanhtoanActionPerformed

