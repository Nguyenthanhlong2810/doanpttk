    private void btnThanhtienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhtienActionPerformed
        // TODO add your handling code here:
        int giamgia=0;
        int diemtichluy=0;
        int tonghoadon=Integer.parseInt(txtTongcong.getText());
        if(txtMakm.getText().length()>0){
            MaKM km=MaKmModify.findByID(txtMakm.getText());
            int value=km.getGiaTri();
            giamgia=(tonghoadon*value)/100;
        }
        if(checkDiemtichluy.isSelected()){
            diemtichluy=Integer.parseInt(txtDiemtichluy.getText());
        }    
        tonghoadon=tonghoadon-giamgia-diemtichluy;
        giamGia=giamgia+diemtichluy;
        txtTonghoadon.setText(""+tonghoadon);
       
    }//GEN-LAST:event_btnThanhtienActionPerformed

