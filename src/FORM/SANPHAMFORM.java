    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        List<SanPham> spList=SanPhamModify.findAll();
        selectedIndex=tblSanpham.getSelectedRow();
       
        if(selectedIndex>=0){
            SanPham sp=spList.get(selectedIndex);
            
            int option=JOptionPane.showConfirmDialog(this, "BẠN CÓ MUỐN XÓA SẢN PHẨM NÀY ?");
          
            
            if(option==0){
              SanPhamModify.delete(sp.getMaSP());
              JOptionPane.showMessageDialog(rootPane, "XÓA THÀNH CÔNG !");
              showSanPham();
              showInfo();
            }
        }else{
            SanPham sp=SanPhamModify.findByID(txtMasp.getText());
            int option=JOptionPane.showConfirmDialog(this, "BẠN CÓ MUỐN XÓA SẢN PHẨM NÀY ?");
         
            SanPhamModify.delete(sp.getMaSP());
            JOptionPane.showMessageDialog(rootPane, "XÓA THÀNH CÔNG!");
            showSanPham();
            showInfo();
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

