    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
         String input =JOptionPane.showInputDialog(this,"NHẬP TÊN SẢN PHẨM CẦN TÌM KIẾM:");
        if(input!=null&&input.length()>0){
            List<SanPham> spList=SanPhamModify.findByName(input);
            
            tableModel.setRowCount(0);
            spList.forEach(sp -> {
            tableModel.addRow(new Object[] {sp.getMaSP(),
                sp.getTenSP(),
                LoaiSPModify.findByID(sp.getMaLoaiSP()).getTenLoaiSP(),
                NhaCCModify.findByID(sp.getMaNCC()).getTenNCC(),
                sp.getGiaNhap(),sp.getGiaXuat(),
                sp.getDonVi(),sp.getSoLuong()
            });
           
        });
        }else{
            showSanPham();
    }                   
    }//GEN-LAST:event_btnFindActionPerformed

