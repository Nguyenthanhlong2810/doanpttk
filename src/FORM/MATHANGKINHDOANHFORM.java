    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String maLsp=txtMalsp.getText();
        String tenLsp=txtTenlsp.getText();
        
        boolean isOK=true;
        
        if(LoaiSPModify.compareByID(maLsp)){
            JOptionPane.showMessageDialog(rootPane, "MÃ LOẠI SẢN PHẨM ĐÃ TỒN TẠI !");
            isOK=false;
            
        }
        if(isOK){
        LoaiSanPham lsp=new LoaiSanPham(maLsp,tenLsp);
        
        LoaiSPModify.insert(lsp);
        JOptionPane.showMessageDialog(rootPane, "LƯU THÀNH CÔNG !");
        }
        showLoaiSP();
    }//GEN-LAST:event_btnSaveActionPerformed

