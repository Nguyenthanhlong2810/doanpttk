    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         List<LoaiSanPham> lspList=LoaiSPModify.findAll();
        selectedIndex=tblLoaisp.getSelectedRow();
       
        if(selectedIndex>=0){
            LoaiSanPham lsp=lspList.get(selectedIndex);
            
            int option=JOptionPane.showConfirmDialog(this, "BẠN CÓ MUỐN XÓA LOẠI SẢN PHẨM NÀY ?");
          
            
            if(option==0){
              LoaiSPModify.delete(lsp.getMaLoaiSP());
              JOptionPane.showMessageDialog(rootPane, "XÓA THÀNH CÔNG!");
              showLoaiSP();
              showInfo();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

