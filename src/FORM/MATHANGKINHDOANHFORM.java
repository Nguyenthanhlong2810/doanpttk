     private void showLoaiSP(){
        List<LoaiSanPham> lspList=LoaiSPModify.findAll();
        tableModel.setRowCount(0);
        lspList.forEach(lsp -> {
            tableModel.addRow(new Object[] {lsp.getMaLoaiSP(),
                lsp.getTenLoaiSP()
            });
           
        });
    }
     
    private void showSelectedRow(){
        selectedIndex=tblLoaisp.getSelectedRow();
       
        if(selectedIndex>=0){
            txtMalsp.setText((String) tblLoaisp.getValueAt(selectedIndex,0));
            txtTenlsp.setText((String) tblLoaisp.getValueAt(selectedIndex,1));
            
        }else{
            showInfo();
        }
    }
    private void showInfo(){
       List<LoaiSanPham> lspList=LoaiSPModify.findAll();
    
       try{
            LoaiSanPham lsp=lspList.get(lspList.size()-1);
            txtMalsp.setText(lsp.getMaLoaiSP());
            txtTenlsp.setText(lsp.getTenLoaiSP());
            
       }catch(Exception ex){
           txtMalsp.setText("");
           txtTenlsp.setText("");
       }
        
    }
    private void showSanPham(String maloaisp){
        List<SanPham> spList=SanPhamModify.findByMaLoaiSP(maloaisp);
        tableModelSP.setRowCount(0);
        spList.forEach(sp -> {
            tableModelSP.addRow(new Object[] {sp.getMaSP(),
                sp.getTenSP(),sp.getGiaXuat(),sp.getMaNCC(),sp.getSoLuong()
            });         
        });
    }
