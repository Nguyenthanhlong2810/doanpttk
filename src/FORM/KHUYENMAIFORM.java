    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String maKm=txtMakm.getText();
        int giaTri=0;
        String ngayBd=txtNgaybd.getText();
        String ngayKt=txtNgaykt.getText();
        String ghiChu=txtNoidung.getText();
        boolean isOK=true;
    
        if(MaKmModify.compareByID(maKm)){
            JOptionPane.showMessageDialog(rootPane, "MÃ KHUYẾN MẠI NÀY ĐÃ TỒN TẠI !");
            isOK=false;            
        }

        try{
            giaTri=Integer.parseInt(txtGiatri.getText());
        }catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, "GIÁ TRỊ NÀY PHẢI LÀ SỐ!");
            isOK=false;    
        }
        try{
        Date date=Date.valueOf(txtNgaybd.getText());
        }
        catch(java.lang.IllegalArgumentException ex){
            JOptionPane.showMessageDialog(rootPane, "NHẬP SAI ĐỊNH DẠNG NGÀY BẮT ĐẦU (YYYY-MM-DD)!");
            isOK=false;
        }
        
        try{
        Date date=Date.valueOf(txtNgaykt.getText());
        }
        catch(java.lang.IllegalArgumentException ex){
            JOptionPane.showMessageDialog(rootPane, "NHẬP SAI ĐỊNH DẠNG NGÀY KẾT THÚC (YYYY-MM-DD)!");
            isOK=false;
        }
        if(isOK){
        MaKM km=new MaKM(maKm,giaTri,ghiChu,ngayBd,ngayKt);
        MaKmModify.insert(km);
        JOptionPane.showMessageDialog(rootPane, "LƯU THÀNH CÔNG !");
       }
       
       
        showMakm();
    }//GEN-LAST:event_btnSaveActionPerformed

