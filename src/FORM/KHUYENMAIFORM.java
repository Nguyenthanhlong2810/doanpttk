    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        List<MaKM> kmList=MaKmModify.findAll();
        selectedIndex=tblMakm.getSelectedRow();
       
        if(selectedIndex>=0){
            MaKM km=kmList.get(selectedIndex);
            
            int option=JOptionPane.showConfirmDialog(this, "BẠN CÓ MUỐN XÓA MÃ KHUYẾN MÃI NÀY?");
          
            
            if(option==0){
              MaKmModify.delete(km.getMaKM());
              JOptionPane.showMessageDialog(rootPane, "XÓA THÀNH CÔNG!");
              showMakm();
              showInfo();
            }
        }
        else{
            MaKmModify.delete(txtMakm.getText());
            JOptionPane.showMessageDialog(rootPane, "XÓA THÀNH CÔNG!");
            showMakm();
            showInfo();
            
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

