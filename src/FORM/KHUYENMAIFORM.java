    private void showInfo(){
          List<MaKM> kmList=MaKmModify.findAll();
    
       try{
            MaKM km=kmList.get(kmList.size()-1);
            txtMakm.setText(km.getMaKM());
            txtGiatri.setText(""+km.getGiaTri());
            txtNgaybd.setText(""+km.getNgayBD());
            txtNgaykt.setText(""+km.getNgayKT());
            txtNoidung.setText(km.getGhiChu());
       }catch(Exception ex){
            txtMakm.setText("");
            txtGiatri.setText("");
            txtNgaybd.setText("");
            txtNgaykt.setText("");
            txtNoidung.setText("");
       }
    }
    private void showMakm(){
        List<MaKM> kmList=MaKmModify.findAll();
        tableModel.setRowCount(0);
        kmList.forEach(km -> {
            tableModel.addRow(new Object[] {km.getMaKM(),
                km.getGiaTri(),km.getNgayBD(),
                km.getNgayKT(),km.getGhiChu()
            });          
        });
    }
    private void showSelectedRow(){
        List<MaKM> kmList=MaKmModify.findAll();
        selectedIndex=tblMakm.getSelectedRow();
       
        if(selectedIndex>=0){
            MaKM km=kmList.get(selectedIndex);
            txtMakm.setText(km.getMaKM());
            txtGiatri.setText(""+km.getGiaTri());
            txtNgaybd.setText(""+km.getNgayBD());
            txtNgaykt.setText(""+km.getNgayKT());
            txtNoidung.setText(km.getGhiChu());
        }
    }
