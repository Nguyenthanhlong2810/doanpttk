     private void showKhachHang(){
        List<KhachHang> khachHangList=KhachHangModify.findAll();
        tableModel.setRowCount(0);
        khachHangList.forEach(kh -> {
            tableModel.addRow(new Object[] {kh.getMaKH(),
                kh.getTenKH(),kh.getDiaChi(),kh.getSdt(),
                kh.getDiemTichLuy()
            });
           
        });
    }
    private void showSelectedRow(){
        selectedIndex=tblKhachHang.getSelectedRow();    
        if(selectedIndex>=0){
                String makh=(String) tblKhachHang.getValueAt(selectedIndex,0);
                KhachHang kh=KhachHangModify.findByID(makh);
                txtMakh.setText(makh);                
                txtTenkh.setText(kh.getTenKH());             
                txtDiachi.setText(kh.getDiaChi());              
                txtSdt.setText(kh.getSdt());
                txtDiemtichluy.setText(""+kh.getDiemTichLuy());
            }
        else{
            showKhachHang();
        }
            
    }
    private void showInfo(){
       List<KhachHang> khList=KhachHangModify.findAll();
    
       try{
           KhachHang kh=khList.get(khList.size()-1);
            txtMakh.setText(kh.getMaKH());
            txtTenkh.setText(kh.getTenKH());
            txtDiachi.setText(kh.getDiaChi());
            txtSdt.setText(kh.getSdt());
            txtDiemtichluy.setText(""+kh.getDiemTichLuy());
       }catch(Exception ex){
           txtMakh.setText("");
           txtTenkh.setText("");
           txtDiachi.setText("");
           txtSdt.setText("");
           txtDiemtichluy.setText("");
       }
        
    }
