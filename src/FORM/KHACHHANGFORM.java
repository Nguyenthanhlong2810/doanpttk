    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String maKH=txtMakh.getText();
        String tenKH=txtTenkh.getText();
        String diaChi=txtDiachi.getText();
        String sdt=txtSdt.getText();
        int diemtichluy=0;
        boolean isOK=true;
        try{
            Double.parseDouble(sdt);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, "SỐ ĐIỆN THOẠI KHÔNG ĐƯỢC CHỨA KÍ TỰ HOẶC BỎ TRỐNG!");
            isOK=false;
        }
        if(txtTenkh.getText()==null||txtTenkh.getText().length()<=0){
            JOptionPane.showMessageDialog(rootPane, "VUI LÒNG NHẬP TÊN KHÁCH HÀNG!");
            isOK=false;
        }
        if(KhachHangModify.compareByID(maKH)){
            JOptionPane.showMessageDialog(rootPane, "MÃ KHÁCH HÀNG ĐÃ TỒN TẠI!");
            isOK=false;
            
        }
        if(isOK){
        KhachHang kh=new KhachHang(maKH,tenKH,diaChi,sdt,diemtichluy);
        
        KhachHangModify.insert(kh);
        JOptionPane.showMessageDialog(rootPane, "LƯU THÀNH CÔNG!");
        }
        showKhachHang();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        List<KhachHang> khachHangList=KhachHangModify.findAll();
        selectedIndex=tblKhachHang.getSelectedRow();
       
        if(selectedIndex>=0){
            KhachHang kh=khachHangList.get(selectedIndex);
            
            int option=JOptionPane.showConfirmDialog(this, "BẠN CÓ MUỐN XÓA KHÁCH HÀNG NÀY?");
          
            
            if(option==0){
              KhachHangModify.delete(kh.getMaKH());
              JOptionPane.showMessageDialog(rootPane, "XÓA THÀNH CÔNG!");
              showKhachHang();
              showInfo();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
          String input =JOptionPane.showInputDialog(this,"NHẬP TÊN KHÁCH HÀNG CẦN TÌM KIẾM:");
        if(input!=null&&input.length()>0){
            List<KhachHang> khachHangList=KhachHangModify.findByName(input);
            
            tableModel.setRowCount(0);
            khachHangList.forEach(kh -> {
            tableModel.addRow(new Object[] {kh.getMaKH(),
                kh.getTenKH(),kh.getDiaChi(),kh.getSdt(),
                kh.getDiemTichLuy()
            });
           
        });
        }else{
            showKhachHang();
    }//GEN-LAST:event_btnFindActionPerformed
    }
